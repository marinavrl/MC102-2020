def escrever_imagem_decodificada(largura, altura, imagem, nome_do_arquivo):
    with open(nome_do_arquivo, "w") as arquivo:
        arquivo.write('P1\n')
        arquivo.write(f'{largura} {altura}\n')
        for i in range(len(imagem)):
            for linha0 in imagem[i]:
                linha0 = ''.join(linha0)
            linha = linha0 + "\n"
            arquivo.write(linha)
    return arquivo

